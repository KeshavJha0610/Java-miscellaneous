package javamiscellaneous;

public class Prob2 {

    public static void main(String[] args) {
        int k = 1;
        for (int i = 1; i <= 4; i++) {
            for (int j = 0; j < i; j++) {
                System.out.print(k);
                System.out.print("\t");//\t is for space
                k++;
            }
            System.out.println("");
        }

    }

}
